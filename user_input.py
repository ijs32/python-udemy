import math
miles = input("enter miles to convert to kilometers: ")
kilometers = int(miles) * 1.60934

print("{} miles is equal to {} kilometers".format(miles, kilometers))

# different math functions

print("ceil(4.4) = ", math.ceil(4.4))  # round up to whole number
print("floor(4.4) = ", math.floor(4.4))  # round down to whole number
print("fabs(-4.4) = ", math.fabs(-4.4))  # abs value
print("trunc(-4.4) = ", math.trunc(4.4))  # convert float to int
print("pow(4,4) = ", math.pow(4, 4))  # power of
print("sqrt(4) = ", math.sqrt(4))  # square root of
print("pi = ", math.pi)  # square root of
